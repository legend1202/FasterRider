package repartidor.faster.com.ec.Getset;

public class TravelHistorySet {
    private String status;
    private String detail;
    private String photo;
    private String historyNo;
    private String historyDate;
    private String CashDetail;
    private String CashVoucher;
    private String CashAmount;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getHistoryNo() {
        return historyNo;
    }

    public void setHistoryNo(String historyNo) {
        this.historyNo = historyNo;
    }

    public String getHistoryDate() {
        return historyDate;
    }
    public void setHistoryDate(String historyDate) {
        this.historyDate = historyDate;
    }
    public String getCashDetail() {
        return CashDetail;
    }
    public void setCashDetail(String CashDetail) {
        this.CashDetail = CashDetail;
    }
    public String getCashVoucher() {
        return CashVoucher;
    }
    public void setCashVoucher(String CashVoucher) {
        this.CashVoucher = CashVoucher;
    }
    public String getCashAmount() {
        return CashAmount;
    }
    public void setCashAmount(String CashAmount) {
        this.CashAmount = CashAmount;
    }

}
